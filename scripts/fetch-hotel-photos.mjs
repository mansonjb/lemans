/**
 * Fetches real establishment photos from Google Places API (New), same
 * approach as hotelswithpets: text search -> place -> photo -> download +
 * compress with sharp. Writes /public/images/hotels/<slug>.jpg and a manifest
 * at src/data/hotel-images.ts.
 *
 * Usage:
 *   node scripts/fetch-hotel-photos.mjs           # reads key from .env.local
 *   node scripts/fetch-hotel-photos.mjs --force   # re-download all
 */
import sharp from "sharp";
import { mkdir, readFile, writeFile } from "fs/promises";
import { existsSync, statSync } from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "public", "images", "hotels");
const HOTELS_TS = path.join(ROOT, "src", "data", "hotels.ts");
const MANIFEST_TS = path.join(ROOT, "src", "data", "hotel-images.ts");

const FORCE = process.argv.includes("--force");
const DELAY_MS = 200;
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

const ZONE_TOWN = {
  "circuit-area": "Le Mans",
  "le-mans-city-centre": "Le Mans",
  arnage: "Arnage",
  mulsanne: "Mulsanne",
  ruaudin: "Ruaudin",
  change: "Changé",
  ecommoy: "Écommoy",
  "la-fleche": "La Flèche",
  alencon: "Alençon",
  laval: "Laval",
  tours: "Tours",
  angers: "Angers",
};

const slugify = (name) =>
  name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

async function loadApiKey() {
  if (process.env.GOOGLE_PLACES_API_KEY) return process.env.GOOGLE_PLACES_API_KEY;
  const env = await readFile(path.join(ROOT, ".env.local"), "utf-8");
  const m = env.match(/GOOGLE_PLACES_API_KEY=(.+)/);
  if (!m) throw new Error("No GOOGLE_PLACES_API_KEY in .env.local");
  return m[1].trim();
}

function parseHotels(src) {
  const re =
    /\{\s*name:\s*"([^"]+)",\s*zone:\s*"([^"]+)",\s*category:\s*\d,\s*kind:\s*"([^"]+)"/g;
  const out = [];
  let m;
  while ((m = re.exec(src))) out.push({ name: m[1], zone: m[2], kind: m[3] });
  return out;
}

async function searchPhoto(name, town, apiKey) {
  const res = await fetch("https://places.googleapis.com/v1/places:searchText", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "X-Goog-Api-Key": apiKey,
      "X-Goog-FieldMask": "places.id,places.displayName,places.photos",
    },
    body: JSON.stringify({
      textQuery: `${name}, ${town}, France`,
      languageCode: "en",
    }),
  });
  if (!res.ok) throw new Error(`searchText HTTP ${res.status}: ${(await res.text()).slice(0, 160)}`);
  const data = await res.json();
  const place = data.places?.[0];
  if (!place) throw new Error("no place");
  const photoName = place.photos?.[0]?.name;
  if (!photoName) throw new Error("no photos");
  return { photoName, displayName: place.displayName?.text };
}

async function fetchPhoto(photoName, apiKey) {
  const url = `https://places.googleapis.com/v1/${photoName}/media?maxWidthPx=1200&skipHttpRedirect=true&key=${apiKey}`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`photo media HTTP ${res.status}`);
  const data = await res.json();
  if (!data.photoUri) throw new Error("no photoUri");
  const img = await fetch(data.photoUri);
  if (!img.ok) throw new Error(`image HTTP ${img.status}`);
  return Buffer.from(await img.arrayBuffer());
}

async function save(buffer, outPath) {
  await sharp(buffer)
    .resize(800, 500, { fit: "cover", position: "centre" })
    .jpeg({ quality: 82, progressive: true, mozjpeg: true })
    .toFile(outPath);
  return Math.round(statSync(outPath).size / 1024);
}

const apiKey = await loadApiKey();
const hotels = parseHotels(await readFile(HOTELS_TS, "utf-8"));
await mkdir(OUT_DIR, { recursive: true });

console.log(`\n📸 Le Mans hotel photos — ${hotels.length} establishments | force=${FORCE}\n`);
let ok = 0,
  skip = 0,
  fail = 0;
const have = [];
const failures = [];

for (const h of hotels) {
  const slug = slugify(h.name);
  const outPath = path.join(OUT_DIR, `${slug}.jpg`);
  if (!FORCE && existsSync(outPath)) {
    have.push(slug);
    skip++;
    continue;
  }
  const town = ZONE_TOWN[h.zone] ?? "Le Mans";
  process.stdout.write(`  ${h.name} (${town}) … `);
  try {
    const { photoName, displayName } = await searchPhoto(h.name, town, apiKey);
    const buf = await fetchPhoto(photoName, apiKey);
    const kb = await save(buf, outPath);
    console.log(`✓ ${kb}KB ← ${displayName}`);
    have.push(slug);
    ok++;
  } catch (e) {
    console.log(`✗ ${e.message}`);
    failures.push({ name: h.name, slug, error: e.message });
    fail++;
  }
  await sleep(DELAY_MS);
}

have.sort();
const manifest = `// Auto-generated by scripts/fetch-hotel-photos.mjs.
// Slugs (see hotelSlug) of establishments that have a real downloaded photo
// in /public/images/hotels/<slug>.jpg.
export const HOTEL_IMAGES: string[] = [
${have.map((s) => `  "${s}",`).join("\n")}
];

const SET = new Set(HOTEL_IMAGES);
export const hasHotelImage = (slug: string): boolean => SET.has(slug);
export const hotelImageSrc = (slug: string): string =>
  \`/images/hotels/\${slug}.jpg\`;
`;
await writeFile(MANIFEST_TS, manifest);

console.log(`\n${"─".repeat(50)}`);
console.log(`✅ downloaded ${ok} · skipped ${skip} · failed ${fail}`);
console.log(`📊 with photo: ${have.length}/${hotels.length}`);
if (failures.length)
  await writeFile(
    path.join(__dirname, "_photo-failures.json"),
    JSON.stringify(failures, null, 2)
  );
