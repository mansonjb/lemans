/**
 * Turn an Apify scrape (`scripts/_circuit-<key>.json`, gitignored) into a
 * committed TypeScript data file (`src/data/circuits/<key>.ts`) that ships in
 * the build. Drops outliers, sorts closest-first, derives the zone list with
 * centroid coordinates (so zone pages can centre a map on the town).
 *
 *   node scripts/gen-circuit-ts.mjs silverstone
 */
import fs from "node:fs";
import path from "node:path";

const key = process.argv[2];
if (!key) {
  console.error("usage: node scripts/gen-circuit-ts.mjs <circuit-key>");
  process.exit(1);
}

const MAX_KM = 80; // anything further is a bad geocode / wrong city

const src = path.join("scripts", `_circuit-${key}.json`);
if (!fs.existsSync(src)) {
  console.error(`missing ${src} — run scripts/fetch-circuit-data.mjs first`);
  process.exit(1);
}

const slugify = (s) =>
  s
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");

// Missing, code-like (e.g. "Hb004484"), or non-latin (e.g. Thai script that
// slugifies to nothing) city values fall back to the single catch-all zone.
const cityOf = (raw) =>
  raw && !/\d/.test(raw) && slugify(raw) ? raw : "Circuit area";

const raw = JSON.parse(fs.readFileSync(src, "utf8"));
const dropped = raw.filter((h) => h.distKm > MAX_KM);
const kept = raw
  .filter((h) => h.distKm <= MAX_KM)
  .map((h) => ({ ...h, _city: cityOf(h.city), _zone: slugify(cityOf(h.city)) }))
  .sort((a, b) => a.ring - b.ring || a.distKm - b.distKm || (b.score ?? 0) - (a.score ?? 0));

const hotels = kept.map((h) => ({
  name: h.name,
  slug: h.slug,
  kind: h.kind,
  category: h.category,
  zone: h._zone,
  driveMin: h.driveMin,
  ring: h.ring,
  score: h.score ?? null,
  lat: h.lat,
  lng: h.lng,
  img: h.photo ? `/images/circuits/${key}/${h.slug}.jpg` : "",
}));

// Zones = distinct cities, closest-first, with their best (lowest) drive time
// and the centroid of their stays (for the zone-page map).
const zoneMap = new Map();
for (const h of kept) {
  const z =
    zoneMap.get(h._zone) ??
    { key: h._zone, name: h._city, driveMin: Infinity, ring: 9, count: 0, sLat: 0, sLng: 0 };
  z.driveMin = Math.min(z.driveMin, h.driveMin);
  z.ring = Math.min(z.ring, h.ring);
  z.sLat += h.lat;
  z.sLng += h.lng;
  z.count += 1;
  zoneMap.set(h._zone, z);
}
const zones = [...zoneMap.values()]
  .map((z) => ({
    key: z.key,
    name: z.name,
    driveMin: z.driveMin,
    ring: z.ring,
    count: z.count,
    lat: +(z.sLat / z.count).toFixed(5),
    lng: +(z.sLng / z.count).toFixed(5),
  }))
  .sort((a, b) => a.ring - b.ring || a.driveMin - b.driveMin);

const varName = key.replace(/-/g, "_");
const ts = `// AUTO-GENERATED by scripts/gen-circuit-ts.mjs from an Apify scrape.
// Do not edit by hand — re-run the generator to refresh.
import type { CircuitHotel, CircuitZone } from "@/data/circuit-data";

export const ${varName}Zones: CircuitZone[] = ${JSON.stringify(zones, null, 2)};

export const ${varName}Hotels: CircuitHotel[] = ${JSON.stringify(hotels, null, 2)};
`;

const destDir = path.join("src", "data", "circuits");
fs.mkdirSync(destDir, { recursive: true });
const dest = path.join(destDir, `${key}.ts`);
fs.writeFileSync(dest, ts);

console.log(`✓ ${dest}`);
console.log(`  ${hotels.length} stays · ${zones.length} zones`);
if (dropped.length)
  console.log(`  dropped ${dropped.length}: ${dropped.map((d) => `${d.name} (${d.distKm}km)`).join(", ")}`);
console.log(`  zones: ${zones.map((z) => `${z.name}(${z.count})`).join(", ")}`);
